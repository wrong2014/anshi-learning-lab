def get_uncertainties(category: StuckCategory) -> list[str]:
    """获取某类别的标准不确定性描述"""
    return UNCERTAINTIES.get(category, ["信息不足，需要更多线索才能判断。"])


def get_verification_action(category: StuckCategory, subject: str) -> dict[str, str]:
    """获取某类别+学科的五分钟验证动作"""
    key = f"{subject}_{category.value[0]}"
    default = {
        "title": "用一个错题定位第一处断点",
        "steps": "拿一道最近做错的题，让孩子不看答案，自己指出从哪一行开始和正确答案分叉。不要讲答案，只看他能不能找到。",
    }
    return FIVE_MINUTE_ACTIONS.get(key, default)
